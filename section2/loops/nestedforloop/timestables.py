for i in range(1, 13):
    for j in range(1, 13):
        print(f"{i} * {j} = {i * j}")
    print("-------------------------------")
    
# the outer loop executes the inner loop the number of times defined in the outer loop
# the inner loop executes upto but not including 13 - which is 12


